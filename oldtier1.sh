# ==============================================================================
# 5. INCREMENTAL UPDATE OF FS.PICKLE VIA FSCTL
# ==============================================================================
echo "[+] Injecting new files into existing fs.pickle..."

# We use Python to traverse honeyfs and build a massive block of fsctl commands.
# This prevents us from having to run fsctl 100 times (which is very slow).
cd "$COWRIE_DIR"

FSCTL_CMDS=$(python3 - << 'PYEOF'
import os, sys
honeyfs_dir = "/home/cowrie/cowrie/honeyfs"

# Create a list of fsctl commands
commands = []

for dirpath, dirnames, filenames in os.walk(honeyfs_dir):
    rel_dir = "/" + os.path.relpath(dirpath, honeyfs_dir).replace(os.sep, "/")
    rel_dir = "/" if rel_dir == "/." else rel_dir

    # Ensure directories exist in pickle
    for d in sorted(dirnames):
        full_dir = os.path.join(rel_dir, d).replace("//", "/")
        commands.append(f"mkdir {full_dir}")
        commands.append(f"chown 0:0 {full_dir}")

    # Ensure files exist in pickle
    for f in sorted(filenames):
        real_path = os.path.join(dirpath, f)
        fake_path = os.path.join(rel_dir, f).replace("//", "/")
        size = os.path.getsize(real_path)

        commands.append(f"touch {fake_path} {size}")

        # Adjust owners/permissions for sensitive files
        if "/.ssh" in fake_path or "/.aws" in fake_path or "shadow" in fake_path:
            commands.append(f"chmod 600 {fake_path}")

        if "/home/pi" in fake_path:
            commands.append(f"chown 1000:1000 {fake_path}")
        elif "/home/webadmin" in fake_path:
            commands.append(f"chown 1001:1001 {fake_path}")
        else:
            commands.append(f"chown 0:0 {fake_path}")

# Output commands as a single block for the shell
print("\n".join(commands))
PYEOF
)

# Run the generated commands through Cowrie's fsctl tool
echo "$FSCTL_CMDS" | ./cowrie-env/bin/python ./bin/fsctl "$PICKLE" > /dev/null 2>&1

echo "[+] fs.pickle successfully updated!"

# ==============================================================================
# 6. Restart Cowrie
# ==============================================================================
echo "[+] Restarting Cowrie..."
if [ -f "$COWRIE_DIR/bin/cowrie" ]; then
    "$COWRIE_DIR/bin/cowrie" restart
    sleep 2
    "$COWRIE_DIR/bin/cowrie" status
else
    echo "[!] Restart manually: $COWRIE_DIR/bin/cowrie restart"
fi

echo ""
echo "=== Deception Setup Complete ==="
echo "  Hostname : $NEW_HOSTNAME"
echo "  Banner   : $SSH_BANNER"
echo "  Pickle   : Incrementally updated via fsctl"
echo "  Verify   : ssh root@localhost -p 2222   password: root"
