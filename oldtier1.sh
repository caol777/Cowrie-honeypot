grep -q "^version" "$COWRIE_CFG" || echo "version = $SSH_BANNER" >> "$COWRIE_CFG"

# ==============================================================================
# 2. HoneyFS directories
# ==============================================================================
echo "[+] Creating honeyfs tree..."
mkdir -p "$HONEYFS/etc/cron.d" "$HONEYFS/etc/ssh" "$HONEYFS/etc/apt" \
         "$HONEYFS/var/www/html" "$HONEYFS/var/log" \
         "$HONEYFS/var/sensor-data" "$HONEYFS/proc/net" \
         "$HONEYFS/root/.aws" "$HONEYFS/root/.ssh" \
         "$HONEYFS/home/pi" "$HONEYFS/home/webadmin" "$HONEYFS/opt/sensor"

# ==============================================================================
# 3. Create Bait Files
# ==============================================================================
echo "[+] Writing bait files..."

