# ==============================================================================
# 4. Randomize Timestamps
# ==============================================================================
echo "[+] Setting diverse mtimes on honeyfs files..."
python3 - "$HONEYFS" << 'PYEOF'
import os, sys, random, time
from pathlib import Path
root = Path(sys.argv[1])
random.seed(0xC0DE5CA1)
for p in sorted(root.rglob("*")):
    if p.is_symlink(): continue
    sp = str(p)
    if sp.endswith(".bash_history"): delta = random.uniform(-3600, -60)
    elif "/var/log/" in sp: delta = random.uniform(-7*86400, -60)
    elif "/proc/" in sp: delta = -random.uniform(0, 300)
    else: delta = -random.uniform(30*86400, 180*86400)
    try:
        target = time.time() + delta
        os.utime(p, (target, target))
    except OSError: pass
